经审理查明：2014年4月25日10时许，万宁市公安局和乐派出所的工作人员翁书＊、李觉＊、黄亚＊等人巡逻时，怀疑和乐镇和乐村委会正坡村的王儒雄家有人涉嫌吸毒、贩毒等违法犯罪行为，于是便向房间中的被告人李小锋和王儒雄（另案处理）表明身份，要求对方开门接受调查，但李小锋和王儒雄拒不开门，并分别持一把钩刀镰和菜刀在房间内威胁翁书＊等人，黄亚＊立即用电话向和乐派出所副所长崔文＊汇报，请求支援，崔文＊接汇报后带领派出所的工作人员赶赴现场，王儒雄见状便弃刀逃跑，崔文＊向李小锋表明身份并劝告其放下刀具，配合工作，但李小锋仍继续挥舞手中的钓刀镰砍向崔文＊等人，被崔文＊等人用木棍挡住，尔后，李小锋挥舞着钓镰刀冲出王儒雄家，逃离现场。

以上事实，被告人李小锋在开庭审理过程中亦无异议，且有提取痕迹、物证登记表及物证照片，被告人的常住人口信息表，抓获经过，（2012）万刑初字第378号刑事判决书及释放证明书，证人崔文＊、翁书＊、黄亚＊、李觉＊、陈子＊、王儒雄等人的证言材料，现场勘验笔录及现场照片等证据证实，足于认定。

本院认为，被告人李小锋无视国家法律，以暴力方法阻碍公安民警依法执行职务，其行为已构成了妨害公务罪。公诉机关指控被告人李小锋犯妨害公务罪的事实清楚，证据确实、充分，指控成立。被告人李小锋曾因犯贩卖毒品罪被判处有期徒刑一年七个月，刑满释放后未满五年又犯应当判处有期徒刑以上刑罚之罪，是累犯，应当从重处罚。被告人李小锋在法庭审理过程中，当庭认罪，可以从轻处罚。依照《中华人民共和国刑法》第二百七十七条第一款，第六十五条第一款，第六十七条第三款之规定，判决如下：

被告人李小锋犯妨害公务罪，判处有期徒刑九个月。

